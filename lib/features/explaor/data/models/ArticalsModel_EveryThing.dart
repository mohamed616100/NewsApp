import 'Source_EveryThing.dart';

class ArticlesEveryThing {
  SourceEveryThing? source;
  String? author;
  String? title;
  String? description;
  String? url;
  String? urlToImage;
  String? publishedAt;
  String? content;

  ArticlesEveryThing(
      {this.source,
        this.author,
        this.title,
        this.description,
        this.url,
        this.urlToImage,
        this.publishedAt,
        this.content});

  ArticlesEveryThing.fromJson(Map<String, dynamic> json) {
    source =
    json['source'] != null ? SourceEveryThing.fromJson(json['source']) : null;
    author = json['author'];
    title = json['title'];
    description = json['description'];
    url = json['url'];
    urlToImage = json['urlToImage'];
    publishedAt = json['publishedAt'];
    content = json['content'];
  }

}
